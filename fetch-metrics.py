def fetch_all_instances(project_id):
    transport = RequestsHTTPTransport(
        url="http://localhost:8087/graphql",
        verify=True,
        retries=3,
    )

    # Create a GraphQL client
    client = Client(transport=transport, fetch_schema_from_transport=True)

    # Define the query
    query = gql("""
    query ($projectId: String!) {
      project(projectId: $projectId) {
        name
        id
        dryRuns {
          id
          createdAt
          nodes {
            displayName
            startedAt
            finishedAt
            duration
            type
            ... on DryRunNodePod {
              displayName
              type
              startedAt
              finishedAt
              duration
              phase
              outputArtifacts {
                key
                name
                url
              }
            }
          }
        }
      }
    }
    """)

    variables = {"projectId": project_id}
    try:
        result = client.execute(query, variable_values=variables)
        # Deserialize JSON to Response object
        response = from_dict(data_class=Data, data=result)
        for r in response.project.dryRuns:
            [log, start, end, duration, steps] = process_nodes(r.nodes, "main-logs")
            res = fetch_cpu_memory_metrics(end, response.project.name, duration)
            current = {
                "instance": response.project.name,
                "input": "",
                "dryrun_name": r.id,
                "log_url": log.url,
                "start": start,
                "end": end,
                "duration": duration,
                "sum_of_cpu": res['cpu'],
                "average_memory": res['memory'],
                "pipeline_steps": steps
            }
            test_obj.append(current)

        return response
    except Exception as e:
        print(f"An error occurred: {e.with_traceback()}")


def process_nodes(data, target_name):
    artifact_object: Optional[OutputArtifact] = None
    start = ""
    end = ""
    duration = 0
    steps = []
    for node in data:
        if node.displayName in ["p1-01-retrieve-meta", "p1-02-prepare-meta", "p2-01-retrieve", "p2-02-slice",
                                "p2-03-prepare"] and node.duration:
            duration += int(node.duration)
            steps.append({"step": node.displayName, "duration": node.duration})

        if node.displayName == "p1-01-retrieve-meta":
            start = node.startedAt
            for artifact in node.outputArtifacts:
                if artifact.name == target_name:
                    artifact_object = artifact
        if node.displayName == "p2-03-prepare":
            end = node.finishedAt

    return [artifact_object, start, end, duration, steps]


def fetch_cpu_memory_metrics(finished, node, duration):
    # Queries
    queries = [
        f'sum(rate(container_cpu_usage_seconds_total{{node="{node}"}}[{duration}s]))',
        f'avg(avg_over_time(container_memory_usage_bytes{{node="{node}"}}[{duration}s]))'
    ]

    # Convert ISO 8601 to UTC-aware datetime objects
    end_dt = datetime.strptime(finished, "%Y-%m-%dT%H:%M:%SZ").replace(tzinfo=timezone.utc)

    rq = {"memory": "", "cpu": ""}
    for q in queries:
        query_resp = fetch_data(q, end_dt)['data']['result']
        if len(query_resp) < 1:
            continue
        if q.startswith("avg") and query_resp:
            rq["memory"] = query_resp[0]['value'][1]
        if q.startswith("sum") and query_resp:
            rq["cpu"] = query_resp[0]['value'][1]
    return rq


# Function to fetch data
def fetch_data(query, end_unix):
    # Prometheus server URL
    url = "http://localhost:8086/api/v1/query"

    # Convert to UNIX timestamps
    end_unix = int(end_unix.timestamp())

    # Define parameters
    params_template = {
        "query": query,
        "time": end_unix,
        "step": "15s"
    }
    params = params_template.copy()
    params["query"] = query
    response = requests.get(url, params=params)
    print(response.request.url, response.text)
    return response.json() if response.status_code == 200 else None


def read_content(url):
    import requests

    site_request = requests.get(url)
    site_response = str(site_request.content)
    line = site_response.split("->")[10]
    return line[11:15]


